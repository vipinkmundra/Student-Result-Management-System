    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String rollno = jTextField6.getText();
        String physics = jTextField1.getText();
        String chemistry = jTextField2.getText();
        String maths = jTextField3.getText();
        String em = jTextField4.getText();
        String dbms = jTextField5.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Vkmundra@1");
            
            Statement st = con.createStatement();
            ResultSet res = st.executeQuery("select * from student where rollNo ='"+rollno+"'");
            
            if(res.next()){
                st.executeUpdate("insert into result(rollno,physics,chemistry,maths,em,dbms) values('"+rollno+"','"+physics+"','"+chemistry+"','"+maths+"','"+em+"','"+dbms+"')");
                JOptionPane.showMessageDialog(null, "Succesfully added the data");
                setVisible(false);
                new insertNewResult().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "This rollNo doesn't exist");
            }
            
        }catch(Exception e){
             JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

